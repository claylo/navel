// ── Table of Contents dropdown ────────────────────────────────────────
// Placeholder is stamped into each page, then replaced in a second pass
// once all titles are known from rendered HTML.

const TOC_PLACEHOLDER = '<!--TOC-->';

function buildTocHtml(currentSlug, titleMap) {
  let html = '';
  let lastGroup = '';
  for (const slug of NAV_ORDER) {
    const group = NAV_GROUP_MAP[slug];
    if (!titleMap[slug]) continue; // skip pages that weren't rendered
    if (group !== lastGroup) {
      html += `<div class="toc-group-label">${group}</div>\n`;
      lastGroup = group;
    }
    const label = titleMap[slug];
    const current = slug === currentSlug ? ' class="toc-current"' : '';
    html += `<a href="${slug}.html"${current}>${label}</a>\n`;
  }
  // Colophon at the end
  const colCurrent = currentSlug === 'colophon' ? ' class="toc-current"' : '';
  html += `<div class="toc-group-label">About</div>\n`;
  html += `<a href="colophon.html"${colCurrent}>Colophon</a>\n`;
  return html;
}

const CHEVRON_SVG = '<svg viewBox="0 0 10 6" fill="none"><path d="M1 1L5 5L9 1" stroke="currentColor" stroke-width="1.5" stroke-linecap="round" stroke-linejoin="round"/></svg>';

const TOC_SCRIPT = `<script>
(function() {
  var btn = document.getElementById('toc-btn');
  var panel = document.getElementById('toc-panel');
  if (!btn || !panel) return;
  btn.addEventListener('click', function(e) {
    e.stopPropagation();
    var open = panel.classList.toggle('open');
    btn.setAttribute('aria-expanded', open);
  });
  document.addEventListener('click', function(e) {
    if (!panel.contains(e.target) && e.target !== btn) {
      panel.classList.remove('open');
      btn.setAttribute('aria-expanded', 'false');
    }
  });
})();
</script>`;

// ── Generation footer ───────────────────────────────────────────────────
// Subtle footer showing generation timestamp and Claude Code version.

const DOCS_VERSION = (() => {
  try {
    const versionsDir = join(REPO_ROOT, "npm", "versions");
    const versions = readdirSync(versionsDir)
      .filter((f) => !f.startsWith("."))
      .sort((a, b) => {
        // Semver-aware sort: compare numeric parts
        const pa = a.replace(/^v/, "").split(".").map(Number);
        const pb = b.replace(/^v/, "").split(".").map(Number);
        for (let i = 0; i < 3; i++) {
          if ((pa[i] || 0) !== (pb[i] || 0)) return (pa[i] || 0) - (pb[i] || 0);
        }
        return 0;
      });
    if (versions.length > 0) return versions[versions.length - 1].replace(/^v/, "");
  } catch {}
  return "unknown";
})();

const GENERATED_AT = new Date().toLocaleString("en-US", {
  year: "numeric",
  month: "short",
  day: "numeric",
  hour: "numeric",
  minute: "2-digit",
  hour12: true,
});

const FOOTER_HTML = `<footer class="docset-footer">Generated ${GENERATED_AT} from Claude Code v${DOCS_VERSION}</footer>`;

// ── HTML template ──────────────────────────────────────────────────────

function htmlPage(title, body, hasTabs) {
  return `<!doctype html>
<html lang="en">
<head>
  <meta charset="utf-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>${title}</title>
  <link rel="stylesheet" href="mintlify.css">
  <link rel="stylesheet" href="theme.css">
</head>
<body class="prose dark:prose-invert">
<div class="docset-logo">
  <div>
    <img class="logo-light" src="logo-light.svg" alt="Claude Code" height="28">
    <img class="logo-dark" src="logo-dark.svg" alt="Claude Code" height="28">
  </div>
  <div class="toc-wrapper">
    <button id="toc-btn" class="toc-button" aria-expanded="false" aria-controls="toc-panel">
      ${CHEVRON_SVG}<span>Table of Contents</span>
    </button>
    <div id="toc-panel" class="toc-panel" role="menu">
      ${TOC_PLACEHOLDER}
    </div>
  </div>
</div>
${body}
${FOOTER_HTML}
${hasTabs ? TAB_SCRIPT : ""}
${COPY_SCRIPT}
${TOC_SCRIPT}
</body>
</html>`;
}

// ── Suppress React DOM property warnings ────────────────────────────────
// Mintlify components use HTML attributes (srcset, fetchpriority) that
// React wants as camelCase (srcSet, fetchPriority). These warnings are
// harmless — renderToStaticMarkup still produces correct HTML.

const _origConsoleError = console.error;
console.error = (...args) => {
  if (typeof args[0] === "string" && args[0].includes("Invalid DOM property")) return;
  _origConsoleError.apply(console, args);
};

// ── Main ───────────────────────────────────────────────────────────────

if (!existsSync(buildDir)) mkdirSync(buildDir, { recursive: true });

// Copy CSS + logo assets
copyFileSync(
  join(dashDir, "node_modules", "@mintlify/components/dist/styles.css"),
  join(buildDir, "mintlify.css"),
);
copyFileSync(join(dashAssetsDir, "theme.css"), join(buildDir, "theme.css"));
copyFileSync(join(assetsDir, "logo-light.svg"), join(buildDir, "logo-light.svg"));
copyFileSync(join(assetsDir, "logo-dark.svg"), join(buildDir, "logo-dark.svg"));

const files = readdirSync(docsDir)
  .filter((f) => f.endsWith(".md"))
  .sort();
console.log(`Rendering ${files.length} docs...`);

let ok = 0;
let failed = 0;
let skipped = 0;
const errors = [];

for (const file of files) {
  const slug = basename(file, ".md");
  const mdPath = join(docsDir, file);
  const outPath = join(buildDir, `${slug}.html`);

  try {
    let md = readFileSync(mdPath, "utf-8");

    // Skip files that are full HTML pages (bad fetch, e.g. changelog)
    if (
      md.trimStart().startsWith("<!DOCTYPE") ||
      md.trimStart().startsWith("<html")
    ) {
      process.stdout.write(`  ~ ${slug} (not MDX, skipped)\n`);
      skipped++;
      continue;
    }

    // Preprocess: strip inline exports with hooks
    md = preprocessMdx(md);

    // Truncate changelog to avoid MDX serializer overflow
    if (slug === "changelog") {
      md = truncateChangelog(md);
    }

    const result = await serialize({ source: md });

    const element = createElement(MDXClient, {
      compiledSource: result.compiledSource,
      frontmatter: result.frontmatter,
      scope: result.scope,
      components: componentMap,
    });

    let body = renderToStaticMarkup(element);

    // Post-process: strip the llms.txt "Documentation Index" blockquote
    // that the fetch script prepends to every doc
    body = body.replace(
      /<blockquote>\s*<h2[^>]*>Documentation Index<\/h2>[\s\S]*?<\/blockquote>/,
      "",
    );

    body = rewriteLinks(body);
    body = injectSectionLabel(body, slug);
    body = convertPageDescription(body);
    body = injectDashAnchors(body);

    const title = extractTitle(body);
    const hasTabs = body.includes('role="tablist"');
    const html = htmlPage(title, body, hasTabs);

    writeFileSync(outPath, html);
    ok++;
    process.stdout.write(`  + ${slug}\n`);
  } catch (err) {
    failed++;
    errors.push({ slug, error: err.message });
    process.stderr.write(`  x ${slug}: ${err.message}\n`);
  }
}

// ── Render colophon page from shared assets ─────────────────────────────
const colophonPath = join(assetsDir, "colophon.md");
if (existsSync(colophonPath)) {
  try {
    const colophonMd = readFileSync(colophonPath, "utf-8");
    const colophonResult = await serialize({ source: colophonMd });
    const colophonEl = createElement(MDXClient, {
      compiledSource: colophonResult.compiledSource,
      frontmatter: colophonResult.frontmatter,
      scope: colophonResult.scope,
      components: componentMap,
    });
    const colophonBody = renderToStaticMarkup(colophonEl);
    const colophonHtml = htmlPage("Colophon", colophonBody, false);
    writeFileSync(join(buildDir, "colophon.html"), colophonHtml);
    process.stdout.write("  + colophon\n");
  } catch (err) {
    process.stderr.write(`  x colophon: ${err.message}\n`);
  }
}

// Write manifest for build-docset to consume.
// Uses NAV_ORDER so entries are inserted into the Dash SQLite index in
// site-navigation order (controls sidebar grouping/ordering in Dash).
const manifest = [];
for (const slug of NAV_ORDER) {
  const outPath = join(buildDir, `${slug}.html`);
  if (!existsSync(outPath)) continue;
  const html = readFileSync(outPath, "utf-8");
  const title = extractTitle(html);
  manifest.push({ slug, title, type: "Guide" });
}
// Also add any rendered pages not in NAV_ORDER (shouldn't happen, but safe)
for (const file of files) {
  const slug = basename(file, ".md");
  if (NAV_ORDER.includes(slug)) continue;
  const outPath = join(buildDir, `${slug}.html`);
  if (!existsSync(outPath)) continue;
  const html = readFileSync(outPath, "utf-8");
  const title = extractTitle(html);
  manifest.push({ slug, title, type: "Guide" });
}
// Add colophon as last entry
if (existsSync(join(buildDir, "colophon.html"))) {
  manifest.push({ slug: "colophon", title: "Colophon", type: "Guide" });
}
writeFileSync(join(buildDir, "index.json"), JSON.stringify(manifest, null, 2));
console.log(`Wrote index.json (${manifest.length} entries)`);

// ── Second pass: inject ToC with real titles ─────────────────────────
const titleMap = {};
for (const entry of manifest) {
  titleMap[entry.slug] = entry.title;
}

for (const entry of manifest) {
  const filePath = join(buildDir, `${entry.slug}.html`);
  let html = readFileSync(filePath, "utf-8");
  if (html.includes(TOC_PLACEHOLDER)) {
    const tocHtml = buildTocHtml(entry.slug, titleMap);
    html = html.replace(TOC_PLACEHOLDER, tocHtml);
    writeFileSync(filePath, html);
  }
}
console.log(`Injected ToC into ${manifest.length} pages`);

console.log(`\nDone: ${ok} rendered, ${skipped} skipped, ${failed} failed.`);
if (errors.length > 0) {
  console.log("\nFailed files:");
  for (const { slug, error } of errors) {
    console.log(`  ${slug}: ${error}`);
  }
}
