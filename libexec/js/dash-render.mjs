#!/usr/bin/env node
// render.mjs — Render all MDX docs to static HTML for the Dash docset.
//
// Reads docs/*.md, renders via @mintlify/mdx + @mintlify/components,
// writes HTML to build/_html/. Also copies CSS assets.
//
// Expects REPO_ROOT env var (set by libexec/dash).

import {
  readFileSync,
  writeFileSync,
  copyFileSync,
  readdirSync,
  mkdirSync,
  existsSync,
} from "node:fs";
import { basename, join } from "node:path";
import {
  rewriteLinks,
  injectSectionLabel as _injectSectionLabel,
  convertPageDescription,
  slugify,
  extractTitle,
  injectDashAnchors,
  preprocessMdx,
  truncateChangelog,
  parseNavTabs,
  semverSort,
} from "./dash-transforms.mjs";

const REPO_ROOT = process.env.REPO_ROOT;
if (!REPO_ROOT) {
  console.error("error: REPO_ROOT not set (run via 'navel dash')");
  process.exit(1);
}

const docsDir = join(REPO_ROOT, "docs");
const buildDir = join(REPO_ROOT, "build", "_html");
const dashDir = join(REPO_ROOT, "dash");
const assetsDir = join(REPO_ROOT, "assets");
const dashAssetsDir = join(dashDir, "assets");

// Resolve dash/node_modules deps by direct dist paths — these packages
// only export ESM via "import" (no CJS "require"), so createRequire can't
// resolve them. Direct file imports bypass the exports map entirely.
const nm = join(dashDir, "node_modules");

const { serialize } = await import(join(nm, "@mintlify/mdx/dist/server/index.js"));
const { MDXClient } = await import(join(nm, "@mintlify/mdx/dist/client/default.js"));
const { renderToStaticMarkup } = await import(join(nm, "react-dom/server.js"));
const { createElement } = await import(join(nm, "react/index.js"));
const components = await import(join(nm, "@mintlify/components/dist/index.js"));

// ── Navigation map (generated by fetch-nav.mjs) ────────────────────────
// Maps each slug to its group label, shown above the h1 on the real site.
// Order matches the site navigation structure — controls Dash sidebar ordering.

const navPath = join(REPO_ROOT, "reports", "nav.json");
if (!existsSync(navPath)) {
  console.error("error: reports/nav.json not found — run 'navel nav sync' first");
  process.exit(1);
}
const { navMap: NAV_GROUP_MAP, navOrder: NAV_ORDER } = JSON.parse(readFileSync(navPath, "utf-8"));

// ── Mintlify component map ─────────────────────────────────────────────

const componentMap = {
  ...components,
  Tab: components.Tabs.Item,
  Step: components.Steps.Item,
  AccordionGroup: components.Accordion.Group,
  CardGroup: ({ children }) =>
    createElement("div", { className: "grid grid-cols-2 gap-4" }, children),
  MCPServersTable: () =>
    createElement(
      "p",
      { className: "text-sm italic text-stone-500" },
      "(See MCP documentation for server details)",
    ),
};

// ── Tab switching script (inline, for static-rendered Mintlify Tabs) ───

const TAB_SCRIPT = `<script>
(function() {
  var ACTIVE_CLASSES = 'border-current text-primary dark:text-primary-light';
  var INACTIVE_CLASSES = 'border-transparent text-stone-900 hover:border-stone-300 dark:text-stone-200 dark:hover:border-stone-700';
  var ACTIVE_SET = ACTIVE_CLASSES.split(' ');
  var INACTIVE_SET = INACTIVE_CLASSES.split(' ');

  document.querySelectorAll('[role="tablist"]').forEach(function(tablist) {
    var tabs = Array.from(tablist.querySelectorAll('[role="tab"]'));
    var panelsContainer = tablist.nextElementSibling;
    if (!panelsContainer) return;
    var panels = Array.from(panelsContainer.children).filter(function(el) { return el.getAttribute('role') === 'tabpanel'; });

    tabs.forEach(function(tab, i) {
      tab.addEventListener('click', function() {
        tabs.forEach(function(t) {
          t.setAttribute('aria-selected', 'false');
          t.setAttribute('tabindex', '-1');
          var btn = t.querySelector('[data-component-part="tab-button"]');
          if (btn) {
            btn.setAttribute('data-active', 'false');
            ACTIVE_SET.forEach(function(c) { btn.classList.remove(c); });
            INACTIVE_SET.forEach(function(c) { btn.classList.add(c); });
          }
        });
        panels.forEach(function(p) {
          p.classList.add('hidden');
          p.setAttribute('aria-hidden', 'true');
          p.setAttribute('tabindex', '-1');
        });
        tab.setAttribute('aria-selected', 'true');
        tab.setAttribute('tabindex', '0');
        var btn = tab.querySelector('[data-component-part="tab-button"]');
        if (btn) {
          btn.setAttribute('data-active', 'true');
          INACTIVE_SET.forEach(function(c) { btn.classList.remove(c); });
          ACTIVE_SET.forEach(function(c) { btn.classList.add(c); });
        }
        if (panels[i]) {
          panels[i].classList.remove('hidden');
          panels[i].setAttribute('aria-hidden', 'false');
          panels[i].setAttribute('tabindex', '0');
        }
      });
    });
  });
})();
</script>`;

// ── Inject section label (wraps imported function with NAV_GROUP_MAP) ──

function injectSectionLabel(html, slug) {
  return _injectSectionLabel(html, slug, NAV_GROUP_MAP);
}

